create table departamento (
	id_departamento SERIAL primary key,
	nombre varchar(50)
);

create table municipio (
 	id_municipio SERIAL primary key,
	nombre VARCHAR(50),
	departamento_id INTEGER REFERENCES departamento(id_departamento)
 );

create table predio (
	numero_predial SERIAL primary key,
	avaluo int,
	nombre varchar(255),
	minicipio_id integer references municipio(id_municipio)
);
 
create table propietario(
	id_propietario SERIAL primary key,
	direccion varchar(255),
	telefono varchar(20),
	email varchar(255)
);

create table juridica (
	id_juridica SERIAL primary key,
	nit varchar(15),
	razon_social varchar(255),
	id_propietario integer references propietario(id_propietario),
	predio_id integer references predio(numero_predial)
);

create table tipo_documento (
	id_tipo_documento SERIAL primary key,
	tipo_documento varchar(50)
);

create table persona_natural (
	id_natural SERIAL primary key,
	nombre varchar(255),
	apellido varchar(255),
	id_tipo_documento integer references tipo_documento(id_tipo_documento),
	numero_documento varchar(20),
	id_propietario integer references propietario(id_propietario),
	predio_id integer references predio(numero_predial)
);

create table tipo_construccion (
	id_tipo_construccion SERIAL primary key,
	tipo_construccion varchar(50)
);

create table construccion (
	id_contruccion SERIAL primary key,
	numero_pisos integer,
	area_total int,
	id_tipo_contruccion integer references tipo_construccion(id_tipo_construccion),
	direccion varchar(50),
	predio_id integer references predio(numero_predial)
);

create table tipo_terreno (
	id_tipo_terreno SERIAL primary key,
	tipo_terreno varchar(50)
);

create table terreno (
	id_terreno SERIAL primary key,
	area_terreno int,
	valor_comercial int,
	fuente_agua_cercana boolean,
	id_tipo_terreno integer references tipo_terreno(id_tipo_terreno),
	tiene_construncciones boolean,
	predio_id integer references predio(numero_predial)
);


